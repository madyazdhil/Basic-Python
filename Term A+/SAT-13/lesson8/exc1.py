# fuction with parameter
def addition(a, b):
    z = a + b
    print(z)

def min(c,d):
    q = c - d
    print(q)

def multiple(e, f):
    w = e * f
    print(w)

#function with return value and parameter
def div(t,y):
    k = t/y
    return k

#calling the fuction with arguments
addition(23,21)
min(34,12)
multiple(22,5)
#calling fuction with return value
division = div(34, 2) #making a variable to store the result of the function
print(division)